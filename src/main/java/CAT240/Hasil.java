package CAT240;

public class Hasil {
    private int jumlahCell;
    private int cellDur;
    private int startRG;
    private double totalRange;
    private double rangeCell;
    private double startAZ;
    private double endAZ;


    public int getJumlahCell() {
        return jumlahCell;
    }

    public void setJumlahCell(int jumlahCell) {
        this.jumlahCell = jumlahCell;
    }

    public int getCellDur() {
        return cellDur;
    }

    public void setCellDur(int cellDur) {
        this.cellDur = cellDur;
    }

    public int getStartRG() {
        return startRG;
    }

    public void setStartRG(int startRG) {
        this.startRG = startRG;
    }

    public double getTotalRange() {
        return totalRange;
    }

    public void setTotalRange(double totalRange) {
        this.totalRange = totalRange;
    }

    public double getRangeCell() {
        return rangeCell;
    }

    public void setRangeCell(double rangeCell) {
        this.rangeCell = rangeCell;
    }

    public double getStartAZ() {
        return startAZ;
    }

    public void setStartAZ(double startAZ) {
        this.startAZ = startAZ;
    }

    public double getEndAZ() {
        return endAZ;
    }

    public void setEndAZ(double endAZ) {
        this.endAZ = endAZ;
    }
}
